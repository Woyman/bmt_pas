          <h4>Sejarah Berdirinya BMT Projo Artha Sejahtera</h4>
          <p>
            BMT Projo Artha Sejahtera adalah suatu lembaga Jasa Keuangan Syari’ah yang sistim operasionalnya berdasarkan pada prinsip-prinsip syariah Islam, tidak menggunakan sistim bunga atau riba dalam pengelolaan pembagian keuntungannya tetapi menggunakan sistim bagi hasil berdasarkan syariat Islam dan saling menguntungkan. 
          </p>
          <p>
            Ide untuk mendirikan BMT ini muncul dari permasalahan di atas yang kemudian di tindaklanjuti oleh seorang tokoh masyarakat Bantul bernama Amir Syarifudin sehingga terbentuklah <b>BMT Projo Artha Sejahtera.</b> Nama BMT ini di ambil oleh Bapak Amir Syarifudin dari bahasa jawa yang artinya <i><b>"Kerajaan uang yang mensejahterakan".</b></i> Dalam proses mendirikan BMT PAS ini di bantu oleh Andi Maryanto dan Slamet Raharjo, yang kemudian keduanya mencari referensi tentang tata cara pendirian dan aturan mainnya diantaranya di peroleh dari Dinas Koperasi Bantul, BMT Bina Rakyat Sejahtera (BRS), Syari’ah Economic Service (SES), BMT Agawe Makmur, BMT Multazam, BMT Al-Ikhlas, Microfine dan Bank Muamalat Indonesia. 
          </p>
          <p>
            Dari beberapa referensi yang diperoleh, pada dasarnya lembaga Jasa Keuangan Syari’ah yang berbentuk BMT adalah sama dengan lembaga keuangan simpan pinjam, karena sampai dengan saat ini masih memakai landasan hukum koperasi, sehingga dalam pendiriannyapun sama dengan pendirian koperasi, yaitu:
          </p>
            <ol>
              <li>Mengumpulkan beberapa orang minimal 3 orang sebagai dewan pendiri.</li>
              <li>Mengumpulkan minimal 20 orang sebagai anggota.</li>
              <li>Rapat untuk menentukan Ketua, sekretaris, bendahara dan dewan pengawas.</li>
              <li>Rapat untuk membuat AD/ART.</li>
              <li>Kemudian AD/ART dan berita acara pembentukan ketua, sekretaris, bendahara dan dewan pengawas serta dilampiri foto kopi KTP anggota yang 20 orang tersebut dibawa ke Dinas Koperasi untuk keperluan legalisasi.</li>
              <li>Biasanya sambil menunggu proses legalisasi sebuah koperasi memulai aktifitasnya lebih dulu.</li>
              <li>Setelah legalisasi turun atau sering disebut dengan memperoleh badan hukum maka kewajiban koperasi adalah menjalankan usahanya sebaik mungkin kemudian mengadakan rapat anggota tiap tahun sekali.</li>
            </ol>

          <p>
            Berdasarkan persyaratan tersebut di atas, maka Tim pendiri (Amir Syarifudin, Andi Maryanto, Slamet Raharjo) segera menindaklanjutinya, yaitu :
          </p>

          <ol>
            <li>Pada tanggal 20 Mei 2006, tim pendiri mengumpulkan 20 Orang anggota untuk rapat pembentukan serta menentukan pengurus. Anggota itu terdiri dari ; Amir Syarifudin, Andi Maryanto, Slamet Raharjo, Muh. Alwi Suwignyo, Bima Sakayo, Mulyono, Agus Effendi, Subaryanto, Rahmadi, Imam Bukhori, Sugiyarto, Triadmoko, Muh. Dhananto, Slamet S,Ag, Agus Sumartono, Jupriyanto, Arif Haryanto, Arif Rahman Hakim, Tyas Wening D, Mein Andreas.</li>
            <li>Pada tanggal 20 Mei 2006 terbentuk pengurus dengan komposisi Ketua ; Amir Syarifudin, Sekretaris Slamet Raharjo, Bendahara Tyas Wening D.</li>
            <li>Pada tanggal 27 Mei 2006, Gempa Bumi meratakan tanah di Kabupaten Bantul, sehingga BMT PAS vakum beberapa bulan.</li>
            <li>Bulan Juli 2006 BMT PAS mulai beroperasi, pengurus menetapkan Andi Maryanto, Sulastri, sudartini dan Slamet sebagai pengelola dengan berkantor di Jl. K.H Mas Mansyur 122 Bejen Bantul.</li>
            <li>Kemudian pada bulan September 2006, Pengurus mengajukan badan hukum koperasi ke Dinas Koperasi Bantul dan di adakan Rapat Anggota pertama yang dihadiri oleh anggota dan utusan Dinas Koperasi (Bapak Petrus) yang memberikan pengarahan tentang Koperasi.</li>
            <li>Pada tanggal 20 Oktober 2006, mendapatkan badan hukum dari Dinas Kopersasi sehingga sejak saat itulah namanya Koperasi Jasa Keuangan Syari’ah BMT Projo Artha Sejahtera resmi berbadan hukum hingga kini nomor badan hukum KJKS BMT Projo Artha Sejahtera adalah 086/ BH/ X/ 2006, dengan alamat kantor Jl. K.H.Mas Mansyur No. 122 Bejen Bantul.</li>
            <li>Setelah badan hukum keluar, formasi pengurus dan pengelola diganti dengan harapan lebih fokus dan berkembang. Dengan komposisi Pengurus (Ketua ; Bima Sakayo, Sekretaris Tyas Wening D, Bendahara Slamet Raharjo), Pengelola ( Andi Maryanto, Suroso, Anton Sulistiyono dan Siti Rohani).</li>
          </ol>

