          <h4>Sejarah Berdirinya BMT Projo Artha Sejahtera</h4>
            <p> BMT Projo Artha Sejahtera adalah Sebuah lembaga keuangan yang menggunakan  prinsip-prinsip syariah Islam, yakni tidak menggunakan sistem bunga atau riba dalam pengelolaan pembagian keuntungannya. Pengelolaan menggunakan sistem bagi hasil keuntungan berdasarkan Fatwa MUI  sehingga saling menguntungkan 
            Pendirian BMT Projo Artha Sejahtera berangkat dari ide untuk memberikan alternatif lembaga keuangan yang lebih mendekati kepada prinsip-prinsip muamalah dalam Islam. Amir Syarifudin bersama  tokoh masyarakat Bantul lainnya menggodok ide membentuk lembaga  BMT. Yang di kemudian hari dikenal sebagai BMT Projo Artha Sejahtera disingkat BMT PAS. 
            Nama BMT ini diambil dari bahasa jawa yang di antara artinya "Kerajaan uang yang mensejahterakan".  BMT PAS diharapkan mampu menjadi tempat berkumpulnya uang, BMT  memiliki fungsi memediasi antara pemilik uang dengan yang membutuhkan pembiayaan, sehingga terjadilah sebuah sinergi yang mensejahterakan secara bersama. 
            Dalam proses mendirikan BMT PAS ini di bantu oleh Andi Maryanto dan Slamet Raharjo. Kedua orang inilah yang kemudian mengawali  mencari referensi tentang tata cara pendirian, aturan perundang-undangannya serta hal-hal teknis di dalam pendirian dan berbagai hal lain yang dibutuhkan. Berbagai referensi akhirnya  di peroleh dari Dinas Koperasi Bantul, BMT Bina Rakyat Sejahtera (BRS), Syari’ah Economic Service (SES), BMT Agawe Makmur, BMT Multazam, BMT Al-Ikhlas, Microfine dan Bank Muamalat Indonesia.
            </p>
            <h5>Dari beberapa referensi yang diperoleh, dimulailah proses pendiriannya, yaitu:</h5>
            <ol>
              <li>Mengumpulkan beberapa orang minimal 3 orang sebagai dewan pendiri.</li>
              <li>Mengumpulkan minimal 20 orang sebagai anggota.</li>
              <li>Rapat untuk menentukan Ketua, sekretaris, bendahara dan dewan pengawas.</li>
              <li>Rapat untuk membuat AD/ART.</li>
              <li>Kemudian AD/ART dan berita acara pembentukan ketua, sekretaris, bendahara dan dewan pengawas serta dilampiri foto kopi KTP anggota yang 20 orang tersebut dibawa ke Dinas Koperasi untuk keperluan legalisasi.</li>
              <li>Biasanya sambil menunggu proses legalisasi sebuah koperasi memulai aktifitasnya lebih dulu.</li>
              <li>Setelah legalisasi turun atau sering disebut dengan memperoleh badan hukum maka kewajiban koperasi adalah menjalankan usahanya sebaik mungkin kemudian mengadakan rapat anggota tiap tahun sekali.</li>
            </ol>

