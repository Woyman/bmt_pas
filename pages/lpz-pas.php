<p><h4><b>Zakat perdagangan</b></h4>
<b>Ketentuan zakat perdagangan :</b><ol>
<li>Nishab zakat perdagangan : 85 gram emas</li>
<li>Haul : 1 tahun</li>
<li>Kadar zakat : 2,5%</li>
<li>Dapat dibayarkan dengan uang atau barang</li>
<li>Dikenakan pada perdagangan maupun perseroan</li></p>

<p><b>Cara perhitungan :</b>
Jumlah harta yang harus dikeluarkan zakatnya = (Modal diputar + keuntungan + piutang) – (hutang + kerugian)</br>
&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp;Zakat = 2,5% x jumlah harta yang harus dikeluarkan zakatnya</p>

<b>Contoh Kasus 1 :</b>
<p>Seorang pedagang menjumlah barang dagangannya pada akhir tahun, diperoleh Rp. 300.000.000. Ia memiliki laba bersih sebesar Rp. 80.000.000. Sementara itu, ia memiliki hutang sebesar Rp. 30.000.000. Berapa besar zakat yang harus dikeluarkan?</p>

<b>Jawab :</b>
<p>Nishab zakat perdagangan adalah 85 gram emas. Jika harga emas Rp. 400.000 per gram, maka nishabnya adalah 85 x Rp. 400.000 = Rp. 34.000.000

</br>Jumlah harta yang harus dikeluarkan zakatnya = (Modal diputar + keuntungan + piutang) – (hutang + kerugian) = (300.000.000 + 80.000.000) – 30.000.000 = 380.000.000 – 30.000.000 = Rp. 350.000.000 (telah melewati nishab)</p>

<p><b>Zakat</b> = 2,5% x jumlah harta yang harus dikeluarkan zakatnya

          = 2,5% x Rp. 350.000.000 = Rp. 8.750.000</p>

<b>Contoh Kasus 2 :</b>

<p>Bapak Andi adalah seorang pedagang grosir. Ia memiliki aset (modal) sebesar Rp. 150.000.000. Setiap bulannya ia mendapatkan keuntungan sebesar Rp. 10.000.000. Ia pun memiliki piutang yang dapat dicairkan sebesar Rp. 20.000.000 dan hutang yang harus dibayar sebesar Rp. 40.000.000. Setelah berjalan 1 tahun, berapa besar zakat yang harus dikeluarkan?
</br></p>
<p>
<b>Jawab :</b>
Nishab = Rp. 34.000.000
Total keuntungan per tahun : Rp. 10.000.000 x 12 bulan = Rp. 120.000.000
Jumlah harta yang harus dikeluarkan zakatnya = (Modal diputar + keuntungan + piutang) – (hutang + kerugian) = (150.000.000 + 120.000.000 + 20.000.000) – (40.000.000) = 290.000.000 – 40.000.000 = Rp. 250.000.000 (te;ah melewati nishab)
</br>Zakat = 2,5% x Rp. 250.000.000 = Rp. 6.250.000</p>


    


